def import_data():
    print("Начинаю импорт категорий и курсов...")
    
    for slug, data in courses_data.items():
        # Создаём или получаем категорию
        category, created = Category.objects.get_or_create(
            slug=slug,
            defaults={
                'name': data['name'],
                'is_active': True
            }
        )
        
        if created:
            print(f"✓ Создана категория: {category.name}")
        else:
            print(f"→ Категория уже существует: {category.name}")
        
        # Добавляем курсы
        for course_data in data['courses']:
            course_slug = f"{slug}-{course_data['title'].lower().replace(' ', '-').replace('(', '').replace(')', '')}"
            
            course, created = Course.objects.get_or_create(
                slug=course_slug,
                defaults={
                    'title': course_data['title'],
                    'category': category,
                    'duration_hours': course_data['duration'],
                    'description': course_data['description'],
                    'short_description': course_data['description'],
                    'is_active': True
                }
            )
            
            if created:
                print(f"  ✓ Добавлен курс: {course.title}")
            else:
                print(f"  → Курс уже существует: {course.title}")
    
    print("\nИмпорт завершён!")

if __name__ == "__main__":
    import_data()